//! Text rendering filter: TextOverlay
//!
//! Uses simple bitmap font rendering directly on the image.
//! No external font loading required — includes a built-in 8×13 bitmap font.

use crate::core::context::{ExecutionContext, ValidationContext};
use crate::core::error::{ExecutionError, ValidationError};
use crate::core::node::{Category, FilterNode, NodeMetadata};
use crate::core::port::{Constraint, ParameterDefinition, PortDefinition, UiHint};
use crate::core::types::{ImageValue, PortType, Value};
use crate::filters::registry::FilterRegistry;

/// Register text filters.
pub fn register(registry: &mut FilterRegistry) {
    registry.register(|| Box::new(TextOverlay));
}

/// Overlay text onto an image using a built-in bitmap font.
#[derive(Debug, Clone)]
pub struct TextOverlay;

impl FilterNode for TextOverlay {
    fn metadata(&self) -> NodeMetadata {
        NodeMetadata::builder("text_overlay", "Text Overlay")
            .description("Add text to an image at a specified position")
            .category(Category::Text)
            .author("Ambara")
            .version("1.0.0")
            .input(
                PortDefinition::input("image", PortType::Image)
                    .with_description("Input image"),
            )
            .output(
                PortDefinition::output("image", PortType::Image)
                    .with_description("Image with text overlay"),
            )
            .parameter(
                ParameterDefinition::new("text", PortType::String, Value::String("Hello Ambara".to_string()))
                    .with_description("Text to draw"),
            )
            .parameter(
                ParameterDefinition::new("x", PortType::Integer, Value::Integer(10))
                    .with_description("X position of text")
                    .with_ui_hint(UiHint::SpinBox)
                    .with_constraint(Constraint::MinValue(0.0)),
            )
            .parameter(
                ParameterDefinition::new("y", PortType::Integer, Value::Integer(10))
                    .with_description("Y position of text")
                    .with_ui_hint(UiHint::SpinBox)
                    .with_constraint(Constraint::MinValue(0.0)),
            )
            .parameter(
                ParameterDefinition::new("scale", PortType::Integer, Value::Integer(2))
                    .with_description("Text scale multiplier (1 = 8px, 2 = 16px, etc.)")
                    .with_ui_hint(UiHint::SpinBox)
                    .with_constraint(Constraint::Range { min: 1.0, max: 10.0 }),
            )
            .parameter(
                ParameterDefinition::new("color_r", PortType::Integer, Value::Integer(255))
                    .with_description("Text color red (0-255)")
                    .with_constraint(Constraint::Range { min: 0.0, max: 255.0 }),
            )
            .parameter(
                ParameterDefinition::new("color_g", PortType::Integer, Value::Integer(255))
                    .with_description("Text color green (0-255)")
                    .with_constraint(Constraint::Range { min: 0.0, max: 255.0 }),
            )
            .parameter(
                ParameterDefinition::new("color_b", PortType::Integer, Value::Integer(255))
                    .with_description("Text color blue (0-255)")
                    .with_constraint(Constraint::Range { min: 0.0, max: 255.0 }),
            )
            .build()
    }

    fn validate(&self, _ctx: &ValidationContext) -> Result<(), ValidationError> {
        Ok(())
    }

    fn execute(&self, ctx: &mut ExecutionContext) -> Result<(), ExecutionError> {
        let image = ctx.get_input_image("image")?;
        let text = ctx.get_string("text").unwrap_or("Hello Ambara");
        let start_x = ctx.get_integer("x").unwrap_or(10).max(0) as u32;
        let start_y = ctx.get_integer("y").unwrap_or(10).max(0) as u32;
        let scale = ctx.get_integer("scale").unwrap_or(2).max(1) as u32;
        let cr = ctx.get_integer("color_r").unwrap_or(255) as u8;
        let cg = ctx.get_integer("color_g").unwrap_or(255) as u8;
        let cb = ctx.get_integer("color_b").unwrap_or(255) as u8;

        let img_data = image.get_image().ok_or_else(|| ExecutionError::NodeExecution {
            node_id: ctx.node_id,
            error: "Image has no data".to_string(),
        })?;

        let mut rgba = img_data.to_rgba8();
        let (w, h) = rgba.dimensions();
        let color = image::Rgba([cr, cg, cb, 255]);

        // Render each character
        let mut cursor_x = start_x;
        for ch in text.chars() {
            let glyph = bitmap_glyph(ch);
            for (row, bits) in glyph.iter().enumerate() {
                for col in 0..8 {
                    if bits & (1 << (7 - col)) != 0 {
                        // Draw a scale×scale block for each lit pixel
                        for sy in 0..scale {
                            for sx in 0..scale {
                                let px = cursor_x + col * scale + sx;
                                let py = start_y + (row as u32) * scale + sy;
                                if px < w && py < h {
                                    rgba.put_pixel(px, py, color);
                                }
                            }
                        }
                    }
                }
            }
            cursor_x += 8 * scale; // advance cursor
        }

        ctx.set_output(
            "image",
            Value::Image(ImageValue::new(image::DynamicImage::ImageRgba8(rgba))),
        )?;
        Ok(())
    }

    fn clone_box(&self) -> Box<dyn FilterNode> {
        Box::new(self.clone())
    }
}

/// Minimal 8×13 bitmap font for ASCII printable characters (32–126).
/// Each glyph is 13 rows of 8-bit masks.
fn bitmap_glyph(ch: char) -> [u8; 13] {
    let idx = ch as u32;
    if !(32..=126).contains(&idx) {
        return FONT_GLYPHS[0]; // space for unknown
    }
    FONT_GLYPHS[(idx - 32) as usize]
}

